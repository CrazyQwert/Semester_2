package vorfuehrAufgabe4;

public class Person {
	
	private String name;
	private int age;
	private boolean kid;
	
	//getter and setter
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public void setKid(boolean kid) {
		this.kid = kid;
	}
	
	public boolean isKid() {
		return this.kid;
	}

}
